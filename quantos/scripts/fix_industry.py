def classify_by_keywords(name: str, symbol: str = "") -> str:
    """根据股票名称关键词推断行业"""
    n = name.replace(" ", "").replace("*", "").replace("ST", "")

    # 按关键词数量打分
    scores = {}
    for industry, keywords in INDUSTRY_KEYWORDS.items():
        if not keywords:
            continue
        score = 0
        matched = []
        for kw in keywords:
            if kw in n:
                score += 1
                matched.append(kw)
        if score > 0:
            # 权重：字符越少匹配越精准
            scores[industry] = (score, len(keywords), matched)

    if not scores:
        return ""

    # 选分数最高（匹配多且关键词列表短=更精准）
    best = sorted(scores.items(), key=lambda x: (x[1][0], -x[1][1]), reverse=True)
    return best[0][0] if best else ""


def main():
    print("=" * 60)
    print("优化行业分类")
    print("=" * 60)

    # 统计当前
    with get_session() as db:
        total = db.query(func.count(StockPoolDB.id)).filter(StockPoolDB.status == 1).scalar()
        empty = db.query(func.count(StockPoolDB.id)).filter(
            StockPoolDB.status == 1,
            StockPoolDB.industry.in_(["综合", ""])
        ).scalar()
        print(f"总股票: {total}, 未精确分类: {empty}")

    # 获取所有未精确分类的股票（避免 session detach）
    with get_session() as db:
        rows = db.query(StockPoolDB.symbol, StockPoolDB.name, StockPoolDB.industry).filter(
            StockPoolDB.status == 1,
            StockPoolDB.industry.in_(["综合", ""]),
        ).all()
        updates = [(r.symbol, r.name) for r in rows]

    updated = 0
    update_map = {}
    for sym, name in updates:
        new_ind = classify_by_keywords(name, sym)
        if new_ind:
            update_map[sym] = new_ind
            updated += 1

    # 批量更新
    with get_session() as db:
        for sym, new_ind in update_map.items():
            db.query(StockPoolDB).filter(
                StockPoolDB.symbol == sym
            ).update({
                StockPoolDB.industry: new_ind,
                StockPoolDB.notes: _industry_desc(new_ind),
            })
        db.commit()

    print(f"重新分类: {updated} 只")

    # 最终统计
    with get_session() as db:
        stats = db.query(
            StockPoolDB.industry,
            func.count(StockPoolDB.id)
        ).filter(StockPoolDB.status == 1).group_by(
            StockPoolDB.industry
        ).order_by(func.count(StockPoolDB.id).desc()).all()

    print("\n最终行业分布:")
    for ind, cnt in stats:
        print(f"  {ind or '(空)':<15} {cnt:>5}")

    # 补全剩余空行业的 notes
    with get_session() as db:
        rows2 = db.query(StockPoolDB.symbol, StockPoolDB.industry).filter(
            StockPoolDB.status == 1,
            StockPoolDB.notes.is_(None),
        ).all()
        for sym, ind in rows2:
            if ind:
                db.query(StockPoolDB).filter(
                    StockPoolDB.symbol == sym
                ).update({StockPoolDB.notes: _industry_desc(ind)})
        db.commit()
        print(f"\n补全描述: {len(rows2)} 只")


if __name__ == "__main__":
    main()
